    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        String updatedJudul = jTextField1.getText();
        String updatedIsi = jTextArea1.getText();

        currentJournal.setJudul(updatedJudul);
        currentJournal.setIsi(updatedIsi);

        new journalController().updateJournal(
            currentJournal.getId(),
            currentJournal.getJudul(),
            currentJournal.getIsi(),
            currentJournal.getFoto()
        );
        JOptionPane.showMessageDialog(this, "Journal updated.");
        this.dispose();
    }                                        

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {                                         
        int confirm = JOptionPane.showConfirmDialog(this, "Are you sure to delete this journal?", "Confirm", JOptionPane.YES_NO_OPTION);
        if (confirm == JOptionPane.YES_OPTION) {
            new journalController().hapusJournal(currentJournal.getId());
            
            JOptionPane.showMessageDialog(this, "Journal deleted.");
            parentList.refreshList();
            this.dispose();
        }
    }                                        

